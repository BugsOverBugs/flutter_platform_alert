/// Represents the button that a user clicks on.
enum AlertButton {
  /// The "abort" button.
  abortButton,

  /// The "cancel" button.
  cancelButton,

  /// The "continue" button.
  continueButton,

  /// The "ignore" button.
  ignoreButton,

  /// The "no" button.
  noButton,

  /// The "ok" button.
  okButton,

  /// The "retry" button.
  retryButton,

  /// The "try again" button.
  tryAgainButton,

  /// The "yes" button.
  yesButton,

  /// Other unknown button.
  other,
}

enum CustomButton {
  positiveButton,
  negativeButton,
  neutralButton,
  other,
}
