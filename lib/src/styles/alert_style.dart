/// Represents the buttons in the dialog/alert.
enum AlertButtonStyle {
  /// Abort, retry, and ignore.
  abortRetryIgnore,

  /// Cancel, try again and continue.
  cancelTryContinue,

  /// Only OK in the dialog.
  ok,

  /// OK and cancel.
  okCancel,

  /// Retry and cancel.
  retryCancel,

  /// Yes and no.
  yesNo,

  /// Yes, no and cancel.
  yesNoCancel,
}
